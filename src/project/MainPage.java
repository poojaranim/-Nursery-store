    private void signUpButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpButtonActionPerformed
        // TODO add your handling code here:
        if(signUpUseridField.getText().equals("")||signUpusernameField.getText().equals("")||signUpEmailField.getText().equals("")
           ||signUpPasswordField.getText().equals(""))
            emptyField.setText("*One or more required fields are empty!");
        else{
            int test = DbConnector.insertToUser(signUpUseridField.getText(), signUpusernameField.getText(),
                signUpEmailField.getText(), signUpPasswordField.getText(),
                signUpmobileNumberField.getText());

            if(test == 19){
                usernameExists.setText("Username exists!");
                emptyField.setText("");
                signUpUseridField.setText("");
            }

            else{
                signUpUseridField.setText("");
                signUpusernameField.setText("");
                signUpEmailField.setText("");
                signUpPasswordField.setText("");
                signUpmobileNumberField.setText("");
                emptyField.setText("");
            }
        }

    }//GEN-LAST:event_signUpButtonActionPerformed

